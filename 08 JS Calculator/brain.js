function add(a, b) {
  return a + b;
}
function subtract(a, b) {
  return a - b;
}
function multiply(a, b) {
  return a * b;
}
function divide(a, b) {
  return a / b;
}
function tip(total) {
  return total * 1.2;
}
function toMi(km) {
  return km * 0.62137;
}
function toKm(miles) {
  return miles / 0.62137;
}
